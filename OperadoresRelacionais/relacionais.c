#include <stdio.h>

int main (){

    /*
   a > b (maior que)
   a < b (menor que)
   a >= b (maior ou igual a)
   a <= b (menor ou igual a)
   a == b (igual a)
   a != b (diferente de)
   */

    int a = 10;
    int b = 20;

    printf("a > b: %d\n", a > b);
    printf("a < b: %d\n", a < b);
    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n", a != b);

    return 0;



}